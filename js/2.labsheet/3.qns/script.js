// Math object demonstration

// Constants
console.log('PI:', Math.PI); // Display the value of PI

// Basic Math functions
console.log('Square root of 16:', Math.sqrt(16)); // Square root
console.log('Power of 2^3:', Math.pow(2, 3)); // Exponential power

// Trigonometric functions (in radians)
console.log('Sine of 30 degrees:', Math.sin((30 * Math.PI) / 180)); // Sine
console.log('Cosine of 45 degrees:', Math.cos((45 * Math.PI) / 180)); // Cosine
console.log('Tangent of 60 degrees:', Math.tan((60 * Math.PI) / 180)); // Tangent

// Random numbers
console.log('Random number between 0 and 1:', Math.random()); // Random number between 0 (inclusive) and 1 (exclusive)
console.log('Random integer between 1 and 10:', Math.floor(Math.random() * 10) + 1); // Random integer between 1 and 10

// Rounding and absolute value
console.log('Round 3.75 to the nearest integer:', Math.round(3.75)); // Round to the nearest integer
console.log('Ceil of 3.2:', Math.ceil(3.2)); // Round up
console.log('Floor of 4.9:', Math.floor(4.9)); // Round down
console.log('Absolute value of -7:', Math.abs(-7)); // Absolute value

// Maximum and minimum
console.log('Maximum of 5, 8, 3, 10:', Math.max(5, 8, 3, 10)); // Maximum
console.log('Minimum of 5, 8, 3, 10:', Math.min(5, 8, 3, 10)); // Minimum
